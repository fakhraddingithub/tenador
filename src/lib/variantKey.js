/**
 * src/lib/variantKey.js
 *
 * کلید پایدار و بدون تداخل برای یک ترکیب واریانت (combination).
 *
 * چرا؟ پیش از این کلیدِ ترکیب با `Object.values(combo).join('-')` ساخته می‌شد که
 * دو مشکل داشت:
 *   ۱) وابسته به ترتیب درج کلیدها بود؛ فرم ویرایش کلید را با ترتیب الفبایی می‌ساخت
 *      ولی تولیدکننده با ترتیب درج → عدم تطابق و گم‌شدن قیمت/تصویرِ ترکیب‌ها.
 *   ۲) اگر دو ویژگی مقدار یکسان داشتند (مثلاً رنگ «M» و سایز «M») یا مقدار شامل «-»
 *      بود، کلیدها با هم تداخل می‌کردند.
 *
 * این تابع کلید را مستقل از ترتیب (مرتب‌شده بر اساس نام ویژگی) و بدون تداخل
 * (نام و مقدار هر دو encode می‌شوند) می‌سازد. خروجی صرفاً برای نگاشتِ داخلی است
 * و هرگز در دیتابیس ذخیره نمی‌شود.
 *
 *   makeComboKey({ color: "قرمز", size: "42" })  →  "color=%D9%82...&size=42"
 *
 * هر دو سرِ مصرف (کلاینت فرم‌ها و سرور route‌ها) باید از همین تابع استفاده کنند.
 */
export function makeComboKey(combo = {}) {
  return Object.keys(combo)
    .sort()
    .map((k) => `${encodeURIComponent(k)}=${encodeURIComponent(combo[k])}`)
    .join("&");
}
